places = ['usa', 'nepal', 'india', 'spain', 'france']

# Orginal List
print(places)
print()

# Sorted List
print(sorted(places))
print()

# Again Original List
print(places)
print()

# Using the sorted to print the reverse alphabetical order
print(sorted(places))
print()

# Once Again the orginal list
print(places)
print()

# Using the reverse function
places.reverse()
print(places)
print()

# Using the reverse for the original list order
places.reverse()
print(places)
print()

# Using the sort method to sort the list in the aplhabetical order
places.sort()
print(places)
print()

# Using the sort method to sort the list in the reverse alphabetical order
places.sort(reverse=True)
print(places)
print()

